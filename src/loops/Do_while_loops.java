package loops;

public class Do_while_loops {

	
	public static void main(String[] args) {
		
	
	// printing table of 7
	// initialization----->7
	// condition------> 70
	// updation-----> s=7
	
	
	
	int s=7;		// initialization
	do 
	{
	System.out.println(s);
	s=s+7;					// updation
		
	}while(s<=70);		//condition
	
	
	
	
	// printing table of 8 in reverse manner
	// initialization-----> 80
	// condition-----> g>=8
	// updation-----> g=g-8
	
	int g=80;
	do
	{
		System.out.println(g);
		g=g-8;
	}while(g>=8);
	
	
	
	
	
	
	
	
}
}
