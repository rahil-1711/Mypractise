package loops;

public class For_loops {
	
	public static void main(String []args)
	{
		
		// syntax ---->  for(initialization;condition;updation)
		//					{	statement		}
		
		
		
		// printing table of 3
			// initialization----->3
			// condition------> x<=30
			// updation------> x=x+3
		
		
		for(int x=3;x<=30;x=x+3)
		{
			System.out.println(x);
		}
		
		
		
		// printing table of 9 in reverse manner
			// initialization----> 90
			// condition-------- m>=9
			// updation------> m=m-9
		
		for (int m=90;m>=9;m=m-9)
		{
			System.out.println(m);
		}
		
		
		
		
		
		
		
		
	}

}
